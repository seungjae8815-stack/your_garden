final Random _promptRng = Random();

/// 오늘 날짜에 해당하는 질문 (하루 동안 고정).
String todaysPrompt([DateTime? now]) {
  final d = (now ?? DateTime.now()).toLocal();
  final dayIndex =
      DateTime(d.year, d.month, d.day).difference(DateTime(2000)).inDays;
  return kDailyPrompts[dayIndex % kDailyPrompts.length];
}

/// '다른 질문 보기' — 지금 질문과 다른 질문을 무작위로 하나 고른다.
String anotherPrompt(String current) {
  if (kDailyPrompts.length < 2) return current;
  String p;
  do {
    p = kDailyPrompts[_promptRng.nextInt(kDailyPrompts.length)];
  } while (p == current);
  return p;
}

/// 기분 5단계 — 정원답게 날씨로 표현.
class Mood {
  const Mood(this.value, this.emoji, this.label);
  final int value; // 1..5
  final String emoji;
  final String label;
}

const List<Mood> kMoods = [
  Mood(1, '🌧️', '많이 힘들어요'),
  Mood(2, '☁️', '가라앉아요'),
  Mood(3, '⛅', '그저 그래요'),
  Mood(4, '🌤️', '괜찮아요'),
  Mood(5, '☀️', '좋아요'),
];

Mood moodOf(int value) =>
    kMoods.firstWhere((m) => m.value == value, orElse: () => kMoods[2]);
